namespace Zion.Vectors
{
    public enum HorizontalDirection : sbyte
    {
        None = 0, Left = -1, Right = 1
    }
    public enum VerticalDirection : sbyte
    {
        None = 0, Down = -1, Up = 1
    }
    public enum Direction : byte
    {
        None = 0,
        Up,
        Right,
        Down,
        Left
    }
}